            Under his reign, the Armenian kingdom expanded beyond its traditional boundaries, allowing
            Tigranes to claim the title "Great King", and involving Armenia in many battles against opponents such
            as the Parthian and Seleucid empires, and the Roman Republic.<br><br>
            At its height, his empire extended from the 
            <a href="https://en.wikipedia.org/wiki/Pontic_Mountains" target="_blank">Pontic Alps</a> (in modern
            north-eastern
            Turkey) to 
            <a href="https://en.wikipedia.org/wiki/Mesopotamia" target="_blank">Mesopotamia</a>,
