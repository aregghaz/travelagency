            title of "King of Kings", which even the Parthian kings did not assume, appearing on coins struck after
            85 BC. Hence, the phrase "sea to sea Armenia" is a popular expression used by Armenians to refer to the
            kingdom of Tigranes, which extended from the Caspian Sea to the Mediterranean Sea.<br><br>
